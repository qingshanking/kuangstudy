public class Demo02 {
    public static void main(String[] args) {
        String a = "hello";
        System.out.println(a);
        int num = 10;
        System.out.println(num);

        //八大基本数据类型
        //整数
        //常用
        int num1 = 10;
        byte num2 = 20;
        short num3 = 30;
        //Long类型要在数字后面加一个L
        long num4 = 50L;
        //小数
        //float 类型要在数字后面加一个F
        float num5 = 50.1F;
        double num6 = 3.1514;
        //字符
        char name = 'A';
        //字符串，String 不是关键词，类
        //String myName = "yanqs";
        //布尔值：是非
        boolean flag = true;
        //boolean flag=false;

    }
}
