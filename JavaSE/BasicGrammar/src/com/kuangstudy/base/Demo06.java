package com.kuangstudy.base;

/**
 * 常量
 *
 * @author yanqs
 * @date 2021-11-14
 */
public class Demo06 {
    /**
     * 修饰符(static、final、public)，不存在先后顺序
     * <p>
     * 使用一些常量，可以提高程序的可读性
     */
    static final double PI = 3.14;

    public static void main(String[] args) {
        System.out.println(PI);
    }
}
